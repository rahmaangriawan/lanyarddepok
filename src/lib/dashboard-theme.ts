const THEME_KEY = "dashboard-theme";

export function getSavedThemeId(): string {
  if (typeof window === "undefined") return "red";
  return localStorage.getItem(THEME_KEY) || "red";
}

export function saveThemeId(id: string): void {
  if (typeof window === "undefined") return;
  localStorage.setItem(THEME_KEY, id);
}

export function getThemeById(id: string): DashboardTheme {
  return dashboardThemes.find((t) => t.id === id) || dashboardThemes[0];
}

export function getThemeCSSVars(theme: DashboardTheme): Record<string, string> {
  // Default red theme uses the CSS-defined values, no overrides needed
  if (theme.id === "red") return {};

  return {
    "--color-brand-red": theme.accent,
    "--color-brand-dark": theme.accentDark,
    "--color-brand-light-50": theme.light50,
    "--color-brand-light-100": theme.light100,
    "--color-brand-light-200": theme.light200,
  };
}
